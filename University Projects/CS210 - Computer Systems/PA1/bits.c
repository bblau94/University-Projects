/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
  int sum_of_xy, sign_of_x, sign_of_y, sign_of_sum_xy;
  sum_of_xy = x + y; //this adds x and y
  sign_of_x = x >> 31; //this records the sign of x
  sign_of_y = y >> 31; //this records the sign of y
  sign_of_sum_xy = sum_of_xy >> 31; //this records the sign of the sum of x & y

  return !(~(sign_of_x ^ sign_of_y) & (sign_of_x ^ sign_of_sum_xy));
//returns the sum of x and y with the proper sign
}

